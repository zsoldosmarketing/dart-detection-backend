function detectBrowserLocale(): string {
  if (typeof window === 'undefined') return 'hu';

  const stored = localStorage.getItem('app-locale');
  if (stored) return stored;

  const forceHungarian = localStorage.getItem('force-hungarian-locale');
  if (forceHungarian === 'true' || forceHungarian === null) {
    localStorage.setItem('app-locale', 'hu');
    localStorage.setItem('force-hungarian-locale', 'true');
    return 'hu';
  }

  const browserLang = navigator.language || (navigator as any).userLanguage || 'hu';
  const langCode = browserLang.toLowerCase().split(/[-_]/)[0];

  if (langCode === 'hu') return 'hu';
  if (langCode === 'en') return 'en';

  return 'hu';
}

let currentLocale = detectBrowserLocale();

let localeChangeListeners: Array<(locale: string) => void> = [];

export function onLocaleChange(callback: (locale: string) => void): () => void {
  localeChangeListeners.push(callback);
  return () => {
    localeChangeListeners = localeChangeListeners.filter(cb => cb !== callback);
  };
}

function updateHtmlLang(locale: string) {
  if (typeof document !== 'undefined') {
    document.documentElement.lang = locale === 'hu' ? 'hu' : 'en';
  }
}

export function setLocale(locale: string) {
  currentLocale = locale;
  if (typeof window !== 'undefined') {
    localStorage.setItem('app-locale', locale);
    updateHtmlLang(locale);
  }

  localeChangeListeners.forEach(callback => callback(locale));
}

if (typeof window !== 'undefined') {
  updateHtmlLang(currentLocale);
}

export function getLocale() {
  return currentLocale;
}

export function t(key: string, params?: Record<string, string | number>): string {
  const translation = translations[currentLocale]?.[key] || translations['hu']?.[key] || key;

  if (params) {
    return Object.entries(params).reduce(
      (str, [k, v]) => str.replace(`{${k}}`, String(v)),
      translation
    );
  }

  return translation;
}

export function addTranslations(locale: string, newTranslations: Record<string, string>) {
  if (!translations[locale]) {
    translations[locale] = {};
  }
  Object.assign(translations[locale], newTranslations);
}
